<template>
    <el-dialog
        v-model="visible"
        :title="titleMap[mode]"
        :width="500"
        destroy-on-close
        @closed="$emit('closed')"
    >
        <el-form
            ref="dialogForm"
            :disabled="mode === 'show'"
            :model="form"
            :rules="rules"
            label-position="left"
            label-width="100px"
        >
            <el-form-item label="角色名称" prop="label">
                <el-input v-model="form.label" clearable></el-input>
            </el-form-item>
            <el-form-item label="角色别名" prop="alias">
                <el-input v-model="form.alias" clearable></el-input>
            </el-form-item>
            <el-form-item label="排序" prop="sort">
                <el-input-number
                    v-model="form.sort"
                    :min="1"
                    controls-position="right"
                    style="width: 100%"
                ></el-input-number>
            </el-form-item>
            <el-form-item label="是否有效" prop="status">
                <el-switch
                    v-model="form.status"
                    active-value="1"
                    inactive-value="0"
                ></el-switch>
            </el-form-item>
            <el-form-item label="备注" prop="remark">
                <el-input
                    v-model="form.remark"
                    clearable
                    type="textarea"
                ></el-input>
            </el-form-item>
        </el-form>
        <template #footer>
            <el-button @click="visible = false">取 消</el-button>
            <el-button
                v-if="mode !== 'show'"
                :loading="isSaving"
                type="primary"
                @click="submit()"
                >保 存
            </el-button>
        </template>
    </el-dialog>
</template>

<script>
export default {
    emits: ["success", "closed"],
    data() {
        return {
            mode: "add",
            titleMap: {
                add: "新增角色",
                edit: "编辑角色",
                show: "查看角色",
            },
            visible: false,
            isSaving: false,
            //表单数据
            form: {
                id: "",
                label: "",
                alias: "",
                sort: 1,
                status: 1,
                remark: "",
            },
            //验证规则
            rules: {
                sort: [
                    {
                        required: true,
                        message: "请输入排序",
                        trigger: "change",
                    },
                ],
                label: [{ required: true, message: "请输入角色名称" }],
                alias: [{ required: true, message: "请输入角色别名" }],
            },
        };
    },
    mounted() {},
    methods: {
        //显示
        open(mode = "add") {
            this.mode = mode;
            this.visible = true;
            return this;
        },
        //表单提交方法
        submit() {
            this.$refs.dialogForm.validate(async (valid) => {
                if (valid) {
                    this.isSaving = true;
                    const res = await this.$API.demo.post.post(this.form);
                    this.isSaving = false;
                    if (res.code === "succeed") {
                        this.$emit("success", this.form, this.mode);
                        this.visible = false;
                        this.$message.success("操作成功");
                    } else {
                        await this.$alert(res.message, "提示", {
                            type: "error",
                        });
                    }
                }
            });
        },
        //表单注入数据
        setData(data) {
            this.form.id = data.id;
            this.form.label = data.label;
            this.form.alias = data.alias;
            this.form.sort = data.sort;
            this.form.status = data.status;
            this.form.remark = data.remark;

            //可以和上面一样单个注入，也可以像下面一样直接合并进去
            //Object.assign(this.form, data)
        },
    },
};
</script>

