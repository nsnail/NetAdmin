<template>
    <el-container>
        <el-aside v-loading="menuloading" width="300px">
            <el-container>
                <el-header>
                    <el-input
                        v-model="menuFilterText"
                        clearable
                        placeholder="输入关键字进行过滤"
                    ></el-input>
                </el-header>
                <el-main class="nopadding">
                    <el-tree
                        ref="menu"
                        :data="menuList"
                        :default-expand-all="true"
                        :expand-on-click-node="false"
                        :filter-node-method="menuFilterNode"
                        :props="menuProps"
                        class="menu"
                        draggable
                        highlight-current
                        node-key="id"
                        show-checkbox
                        @node-click="menuClick"
                        @node-drop="nodeDrop"
                    >
                        <template #default="{ node, data }">
                            <span class="custom-tree-node el-tree-node__label">
                                <span class="label">
                                    {{ node.label }}
                                </span>
                                <span class="do">
                                    <el-icon @click.stop="add(node, data)"
                                        ><el-icon-plus
                                    /></el-icon>
                                </span>
                            </span>
                        </template>
                    </el-tree>
                </el-main>
                <el-footer style="height: 51px">
                    <el-button
                        icon="el-icon-plus"
                        size="small"
                        type="primary"
                        @click="add()"
                    ></el-button>
                    <el-button
                        icon="el-icon-delete"
                        plain
                        size="small"
                        type="danger"
                        @click="delMenu"
                    ></el-button>
                </el-footer>
            </el-container>
        </el-aside>
        <el-container>
            <el-main ref="main" class="nopadding" style="padding: 20px">
                <save
                    ref="save"
                    :menu="menuList"
                    @success="handleSuccess"
                ></save>
            </el-main>
        </el-container>
    </el-container>
</template>
<script>
let newMenuIndex = 1;
import save from "./save";

export default {
    components: {
        save,
    },
    data() {
        return {
            menuloading: false,
            menuList: [],
            menuProps: {
                label: (data) => {
                    return data.meta.title;
                },
            },
            menuFilterText: "",
        };
    },
    watch: {
        menuFilterText(val) {
            this.$refs.menu.filter(val);
        },
    },
    mounted() {
        this.getMenu();
    },
    methods: {
        //加载树数据
        async getMenu() {
            this.menuloading = true;
            const res = await this.$API.sys_menu.query.post();
            this.menuloading = false;
            this.menuList = res.data;
        },
        //树点击
        menuClick(data, node) {
            const pid = node.level === 1 ? 0 : node.parent.data.id;
            this.$refs.save.setData(data, pid);
            this.$refs.main.$el.scrollTop = 0;
        },
        //树过滤
        menuFilterNode(value, data) {
            if (!value) return true;
            const targetText = data.meta.title;
            return targetText.indexOf(value) !== -1;
        },
        //树拖拽
        nodeDrop(draggingNode, dropNode, dropType) {
            this.$refs.save.setData({});
            this.$message(
                `拖拽对象：${draggingNode.data.meta.title}, 释放对象：${dropNode.data.meta.title}, 释放对象的位置：${dropType}`
            );
        },
        //增加
        async add(node, data) {
            const newMenuName = "未命名" + newMenuIndex++;
            const newMenuData = {
                parentId: data ? data.id : 0,
                name: Math.random().toString(),
                path: Math.random().toString(),
                meta: {
                    title: newMenuName,
                    type: "menu",
                },
            };
            this.menuloading = true;
            const res = await this.$API.sys_menu.create.post(newMenuData);
            this.menuloading = false;
            newMenuData.id = res.data.id;
            this.$refs.menu.append(newMenuData, node);
            this.$refs.menu.setCurrentKey(newMenuData.id);
            const pid = node ? node.data.id : "";
            this.$refs.save.setData(newMenuData, pid);
        },
        //删除菜单
        async delMenu() {
            const CheckedNodes = this.$refs.menu.getCheckedNodes();
            if (CheckedNodes.length === 0) {
                this.$message.warning("请选择需要删除的项");
                return false;
            }
            const confirm = await this.$confirm(
                "确认删除已选择的菜单吗？",
                "提示",
                {
                    type: "warning",
                    confirmButtonText: "删除",
                    confirmButtonClass: "el-button--danger",
                }
            ).catch(() => {});
            if (confirm !== "confirm") {
                return false;
            }
            this.menuloading = true;
            const reqData = {
                items: CheckedNodes.map((item) => {
                    return {
                        id: item.id,
                    };
                }),
            };
            try {
                await this.$API.sys_menu.bulkDelete.post(reqData);
                CheckedNodes.forEach((item) => {
                    const node = this.$refs.menu.getNode(item);
                    if (node.isCurrent) {
                        this.$refs.save.setData({});
                    }
                    this.$refs.menu.remove(item);
                });
            } catch {}
            this.menuloading = false;
        },
        handleSuccess() {
            this.$refs.save.setData({});
            this.getMenu();
        },
    },
};
</script>
